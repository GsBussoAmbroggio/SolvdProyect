
    public abstract class Person {

//Attributes
        private int age;
        private String sex;

//Constructor

        public Person(int age, String sex) {
            this.age = age;
            this.sex = sex;
        }


//Getters & Setters

        public int getAge() {
            return age;
        }

        public void setAge(int age) {
            this.age = age;
        }

        public String getSex() {
            return sex;
        }

        public void setSex(String sex) {
            this.sex = sex;
        }
    }

        


